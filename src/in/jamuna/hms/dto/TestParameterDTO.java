package in.jamuna.hms.dto;

public class TestParameterDTO {
	private int id;
	private String name;
	private String unit;
	private Float lowerRange;
	private Float upperRange;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public Float getLowerRange() {
		return lowerRange;
	}
	public void setLowerRange(Float lowerRange) {
		this.lowerRange = lowerRange;
	}
	public Float getUpperRange() {
		return upperRange;
	}
	public void setUpperRange(Float upperRange) {
		this.upperRange = upperRange;
	}
	
	
}
